a = True
b = True

if a == True and b == True:
    print("a 和 b 同時成立")
if a == True and b == False:
    print("a 成立，b 不成立")
if a == False and b == True:
    print("a 不成立，b 成立")
if a == False and b == False:
    print("a 不成立，b 不成立")
if a == True or b == True:
    print("a 或 b 成立")
if a == False or b == False:
    print("a 或 b 不成立")
