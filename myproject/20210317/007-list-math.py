list1 = [7, 3, 2, 5, 1, 6, 4]
list2 = [x+100 for x in list1 if x > 3]
print(list2)  # [107, 105, 106, 104]
list2 = [x for x in list1 if x > 1 and x < 4]
print(list2)  # [3, 2]
list2 = [x*10 for x in list1 if x > 1 and x < 4]
print(list2)  # [30, 20]
list2 = [x for x in list1 if x % 2 == 1]
print(list2)  # [7, 3, 5, 1]
list2 = [x for x in list1 if x < 3]
print(list2)  # [2, 1]
